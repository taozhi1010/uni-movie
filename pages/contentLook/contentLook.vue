沪深300指数期货合约产品：</br>

所谓股指期货，就是以某种股票指数为标的资产的标准化的期货合约。买卖双方报出的价格是一定时期后的股票指数价格水平。在合约到期后，股指期货通过现金结算差价的方式来进行交割。</br> 
沪深300指数由中证指数有限公司编制与维护，成份股票有300只。该指数借鉴了国际市场成熟的编制理念，采用调整股本加权、分级靠档、样本调整缓冲区等先进技术编制而成。中金所首个股指期货合约以沪深300指数为标的物。</br>
上市交易所</br>
中国金融期货交易所</br>
从公布的规则以及合约内容分析，有十大要点值得关注：一、交易时间较股市开盘早15分钟，收盘晚15分钟，用户可利用期指管理风险。</br>
二、涨跌停板幅度为10%，取消熔断，与股票市场保持一致。三、最低交易保证金的收取标准为8%，当前点位单手合约保证金需15-20万元左右。</br>
四、交割日定在每月第三个周五，可规避股市月末波动。五、遇涨跌停板，按“平仓优先、时间优先”原则进行撮合成交。六、每日交易结束后，</br>
将披露活跃合约前20名结算会员的成交量和持有量。七、单个非套保交易账户的持仓限额为100手，当前点位账户限仓金额约在1500万元左右。</br>
八、出现极端行情时，中金所可谨慎使用强制减仓制度控制风险。九、自然人也可以参与套期保值。十、规则为期权等其他创新品种预留了空间。</br>
指期货也有“到期日效应”，许多资金会在到期日平仓，导致价格波动。如果两个市场的波动叠加在一起，会增大市场的波动，对现货市场和期货市场都将产生较大影响</br>
如今将交割日定在第三个周五，基本就可以在当月中旬进行交割，避免出现月末剧烈市场波动。</br>
竞价交易涨跌停板成交“平仓挂单优先”</br>
股指期货连续竞价交易按照“价格优先、时间优先”的原则进行，这一点与A股市场相类似；但在遇到涨跌停板的极端行情之时，以涨跌停板价格申报的指令，</br>
按照“平仓优先、时间优先”的原则进行。这是因为股指期货采用双向交易，用户既可以开多仓，也可以开空仓。</br>
股指期货采用集合竞价和连续竞价两种方式撮合成交，正常交易日9:10-9:15为集合竞价时间。其中，9:10-9:14为指令申报时间，9:14-9:15为指令撮合时间。</br>
集合竞价指令申报时间不接受市价指令申报，集合竞价指令撮合时间不接受指令申报。
为防止部分会员和客户利用技术优势影响交易系统安全和正常交易秩序，对于会员、客户采取可能影响交易所系统安全或者正常交易秩序的方式下达交易指令的，中金所可采取相关措施加以限制。</br>
信息披露</br>
基金、保险头寸可能会“隐藏”起来</br>
任何一张股指期货合约挂牌交易后，只要其单边持仓量达到1万手以上，即被视作“活跃月份合约”。中金所每日交易结束后，将披露活跃月份合约前20名结算会员的成交量和持仓量。</br>
值得一提的是，为保护会员和客户的商业秘密，中金所只公布活跃月份合约前20名结算会员的成交量和持仓量。市场人士推测，基金、保险等大资金未来有望成为与中金所直连的非结算会员，</br>
也就是说，未来基金、保险等大资金的股指期货交易头寸可能会被很好地“隐藏”起来。</br>
这与国内商品期货市场的习惯有很大不同。国内三大商品期货交易所会员分为两种，即自营会员和非自营会员。无论是哪种会员，只要其交易的合约达到信息公开标准，</br>
交易所就会公布其成交量和持仓量。持仓限额单个账户限额约9000万元中金所发布《关于调整沪深300股指期货持仓限额标准的通知》，将沪深300股指期货的持仓限额由1200手调升至5000手。</br>
若以当前点位计算，每手合约面值100万元、保证金比例15%计算，单个交易账户的限仓金额约在9000万元左右。[1] </br>
除了对单个账户进行限仓管理，中金所还将对单个结算会员进行限仓。限仓标准将按照每日结算后，某一合约单边的总持仓量计算。但进行套期保值交易和套利交易的客户号的持仓按照交易所有关规定执行。</br>
为加强对大户的监管，中金所规定从事自营业务的交易会员或者客户不同客户号的持仓及客户在不同会员处的持仓合并计算；增加“客户的持仓尚未满足相关大户标准，但交易所认为有必要的，交易所可以要求其进行大户报告”的规定。</br>
强制减仓</br>
极端行情下谨慎使用强制减仓</br>
借鉴国内期货市场处置极端行情的经验，中金所保留了在连续出现涨跌停板的情况下可以采取强制减仓的制度，即将当日涨跌停板价格申报的为成交平仓单，以当日涨跌停板价格与该合约盈利客户按照持仓比例自动撮合成交。考虑到这种制度虽然化解风险简单有效、但是对于套期保值和套利交易不利，因此中金所只有在出现极端行情时才能谨慎使用。</br>
根据《期货交易所管理办法》第八十六条“期货价格出现同方向连续涨跌停板的，交易所可以采用调整涨跌停板幅度、提高交易保证金标准及按一定原则减仓等措施化解风险”的规定，将股指期货强制减仓的执行条件改为“期货交易连续出现同方向单边市”。</br>
 

