上海证券综合指数简称“上证综指”，其样本股是全部上市股票，包括A股和B股，反映了上海证券交易所上市股票价格的变动情况，自1991年7月15日起正式发布。[1]1历史沿革编辑从1990年至2010年，上海证券交易所(以下简称“上交所”)从最初的8只股票、22只债券，发展为拥有894家上市公司、938只股票、18万亿股票市值的股票市场，拥有199只政府债、284只公司债、25只基金以及回购、权证等交易品种，初步形成以大型蓝筹企业为主，大中小型企业共同发展的多层次蓝筹股市场，是全球增长最快的新兴证券市场。</br>
