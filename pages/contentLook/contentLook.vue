3计算方法编辑计算股票指数，要考虑三个因素：</br>
一是抽样，即在众多股票中抽取少数具有代表性的成份股；</br>
二是加权，按单价或总值加权平均，或不加权平均；</br>
三是计算程序，计算算术平均数、几何平均数，或兼顾价格与总值。</br>
由于上市股票种类繁多，计算全部上市股票的价格平均数或指数的工作是艰巨而复杂的，因此人们常常从上市股票中选择若干种富有代表性的样本股票，并计算这些样本股票的价格平均数或指数。用以表示整个市场的股票价格总趋势及涨跌幅度。计算股价平均数或指数时经常考虑以下四点：</br>
（1）样本股票必须具有典型性、普通性，为此，选择样本对应综合考虑其行业分布、市场影响力、股票等级、适当数量等因素。</br>
（2）计算方法应具有高度的适应性，能对不断变化的股市行情作出相应的调整或修正，使股票指数或平均数有较好的敏感性。</br>
（3） 要有科学的计算依据和手段。计算依据的口径必须统一，一般均以收盘价为计算依据，但随着计算频率的增加，有的以每小时价格甚至更短的时间价格计算。</br>
（4）基期应有较好的均衡性和代表性。</br>
