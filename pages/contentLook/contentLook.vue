股本证券</br>
主板持续做优做强。为筹备创业板，深交所主板市场2000年开始停止新公司上市，当时上市公司514家，总股本为1581亿股，大多集中在传统产业。近年来，通过并购重组、整体上市等方式，实现存量做优做强。截至2014年4月底，深交所主板上市公司480家，总股本4443亿股，2001年以来累计再融资8007亿元。[3]</br>
中小企业板独具特色。中小企业板于2004年5月经国务院批准设立，较好体现了我国深度参与国际分工、产业链拉长、分工更细的成果。截至2014年4月底，上市公司719家，遍布31个省市自治区，其中中西部地区占三分之一多，成为当地经济发展的支柱，覆盖全部13个行业，许多已经成为细分行业龙头。中小板总市值4.17万亿元，成为我国资本市场特色重要组成，在全球范围内，是专注中小企业为数不多的成功市场之一。[3] </br>
创业板定位清晰。创业板历经十年筹备，于2009年10月经国务院批准设立，定位于服务自主创新企业和其他成长型创业企业。截至2014年4月底，创业板上市公司379家，77%所处子行业是“十二五”规划的重点发展方向，超过43%在国际上处于子行业领先或者同步地位。板块特色逐步清晰，在新一轮以创新驱动发展的战略中，将努力打造为科技与金融结合的引领者。截止2014年4月底，创业板总股本758.8亿股，总市值1.88万亿元[3] ，在全球服务创业创新的市场中，市值仅次于NASDAQ，如NASDAQ只计算资本市场板块，则我国创业板规模位居全球第一位。</br>
积极支持场外市场规范建设。从2001开始，深交所承担代办股份转让系统技术支持等工作，2006年1月增加中关村代办系统股份报价转让试点。2012年9月全国中小企业股份转让系统公司设立，深交所参股并继续提供技术支持。同时，深交所积极稳妥支持区域性股权市场规范建设，提供规则、技术、信息披露等方面咨询服务，通过多种渠道支持区域股权市场建设。
固定收益与上市基金</br>
债券产品支持中小企业。债券市场是资本市场服务实体经济的新平台。截至2014年4月底，深交所上市债券442只，托管总面值2159亿元。为拓宽未上市中小微企业融资渠道，2012年推出中小企业私募债券试点，已有200只通过备案，金额270亿元。119家企业完成131只私募债发行，募集资金151亿元，交易所服务促进中小企业规范发展功能逐步显现。[4] 
资产证券化产品服务实体经济。资产证券化业务对于盘活各类社会存量资产有独特优势。截止2013年底，深交所累计挂牌11个资产证券化产品，融资137亿元。基础资产涵盖市政建设、污水处理、高速公路、电力销售等基础设施建设领域与主题公园等文化旅游产业，通过服务基础建设推进新型城镇化进程。首个小额贷款类资产证券化产品阿里小贷1号和2号专项计划在深交所挂牌转让，募集资金10亿元，是小额贷款类资产证券化的首次尝试，对于落实小微企业金融支持具有重要的示范意义。</br>
积极推进基金特色创新。基金产品是顺应财富管理需求和壮大机构用户的重要创新领域，上市基金是深交所基金产品创新的特色。截至2013年底，深交所上市基金数量280只，占深沪两市总量的83.33%；并先后推出跨市场沪深300ETF和跨境香港恒生ETF。</br>

 

