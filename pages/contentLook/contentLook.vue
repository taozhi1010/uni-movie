 1历史沿革编辑第一阶段：诞生（1978-1991）[2] </br>
深交所在改革开放、特区建设和“姓氏”争论中孕育诞生，开创了新中国证券集中交易的先河。资本市场的功能作用，在改革开放和现代化建设实践中不断得到检验。</br>
深圳证券交易所1978年12月，中国共产党十一届三中全会确立以经济建设为中心的基本国策。一年后，改革开放的第一声“开山炮”在深圳蛇口炸响。1980年8月26日，全国人大批准《广东省经济特区条例》，深圳经济特区正式设立。深圳敢闯敢试、敢为天下先的改革精神，海纳百川、兼容并蓄的开放精神，空谈误国、实干兴邦的使命精神，共同孕育了深交所的诞生与成长。</br>
1982年，广东省宝安县联合投资公司在深圳成立，是国内首家通过报刊公开招股的公司，1983年7月向全国发行“深宝安”股票凭证。</br>

1984年10月，中国共产党十二届三中全会通过《关于经济体制改革的决定》，企业股份制开始试点，证券市场也在试点过程中萌动。</br>
1988年4月，“深发展”股票在特区证券挂牌柜台交易，连同随后上柜的“万科”、“金田”、“安达”和“原野”，称“深圳老五股”。</br>
1988年6月至9月，深圳市政府举办四期资本市场基础理论培训班，11月成立资本市场领导小组，在争论中坚持改革探索的脚步。1989年11月25日，下达《关于同意成立深圳证券交易所的批复》。</br>
1990年1月，深圳证券交易所筹备小组正式挂牌办公。</br>
1990年5月28日，深圳市政府通告取缔场外非法交易，其后又进一步决定实行股票买卖价格涨跌停板制度和征收交易印花税，同时也加快了深交所筹备进程。</br>
1990年12月1日，深圳证券交易所试营业。</br>
1990年12月，深圳“股票热”引起党中央、国务院的高度关注，先后对深圳企业股份制改革和证券市场进行了三次调查，最后决定保留上海、深圳股份制及证券市场试点。</br>
1991年4月16日，深圳证券交易所获中国人民银行批准，7月3日正式开业并召开新闻发布会。</br>
1991年7月，深交所主办全国第一家证券理论刊物——《证券市场导报》创刊。</br>
第二阶段：拓荒（1992-1995）</br>
1992年初，邓小平南巡并发表谈话，其中关于股市的论述从根本上消除了人们的疑虑，对证券市场发展起到了巨大的推动作用。</br>
1992年8月，由于新股认购组织不严密，抽签表供不应求，引发了一起群体性事件——深圳“810事件”，表明亟须建立统一规范的监管体系。</br>

1992年10月12日，国务院办公厅发布《关于成立国务院证券委员会的通知》，国务院证券管理委员会和中国证券监督管理委员会先后成立。12月17日，国务院发布《关于进一步加强证券市场宏观管理的通知》，资本市场逐步纳入全国统一监管。1991年7月3日，深交所确立自律监管规则依据——《深圳证券交易所章程》和《深圳证券交易所业务规则》。</br>
1992年12月1日，首创通过电视开展“深圳证券周”活动，普及证券知识，推动理性投资。</br>
1993年4月，国务院颁布《股票发行与交易管理暂行条例》，深圳上市公司率先尝试市场化并购。1993年9月30日，深宝安通过二级市场收购上海延中。1993年11月，深万科收购上海申华实业。1994年3月，君安证券收购深万科。</br>
1993年8月28日，与《人民日报》社联合创立的全国第一家专业证券媒体——《证券时报》试刊。</br>
1994年8月，深交所率先开通证券双向卫星网，突破委托报盘手段瓶颈，覆盖中国大陆所有地区。</br>
建所伊始，深交所就积极探索证券市场国际化道路和产品创新。1992年2月28日“深南玻B”上市，1995年6月在瑞士市场私募发行B股可转债，首创在国际市场直接融资。1993年6月30日，首只纯B股“闽灿坤B”上市。1995年7月12日，“招商局B”首创在新加坡第二上市。在产品创新上，1991年11月1日，盐田港重点建设债券在深交所上市。1992年11月5日，深圳宝安企业（集团）股份有限公司同时发行1992年认股权证及5亿元可转换债券。1994年9月12日，深交所推出国债期货交易业务，首期推出五个系列19个期货品种。到1995年已初步形成A股、B股、债券、认股权证、国债期货等在内的多品种证券市场格局。</br>
