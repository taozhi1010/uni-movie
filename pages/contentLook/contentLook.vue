第四阶段：求索（2000-2009）</br>
20世纪90年代，全球新经济快速发展，中国经济结构加快调整，客观上要求资本市场发挥其促进企业创新和科技进步的重要功能。深交所先后设立中小企业板和创业板，开辟出一条有中国特色的多层次资本市场体系建设道路。</br>
1999年1月15日，深交所向中国证监会提交《关于进行成长板市场方案研究的立项报告》及其实施方案。2000年8月，经国务院同意，中国证监会决定由深交所承担创业板市场筹备任务，同时停止深交所主板新公司上市。</br>
2000年8月，深交所成立创业板筹备工作领导小组，创业板筹备工作全面启动。深交所全面动员，周密组织，在法规规则、技术系统、企业培育、人才储备等方面，为创业板市场建设做了大量基础性工作。</br>
2000年10月26日，举办首期创业板拟上市企业培训班。2001年4月，成立创业企业培训中心。</br>
2001年下半年，全球科技股泡沫破灭，海外创业板市场纷纷失败或步入低谷，在中国建设创业板市场备受质疑。</br>
受多方因素影响，创业板推出进程暂缓。面对困难曲折，深交所积极探索，审时度势，提出分步推进创业板的战略思路，逐步取得广泛共识，为多层次资本市场体系建设铺平了道路。</br>

2002年6月29日，《中小企业促进法》正式颁布。深交所酝酿提出在现有法律框架内先为中小企业设立板块、分步推进创业板的战略思路，2002年11月16日和12月4日分别向中国证监会提交的《关于分步实施推进创业板市场建设的请示》及《关于当前推进创业板市场建设的思考与建议》，并很快取得各方认同。</br>
2003年10月14日，党的十六届三中全会通过《中共中央关于完善社会主义市场经济体制若干问题的决定》，明确提出：“建立多层次资本市场体系，推进风险投资和创业板市场建设”。10月31日，深交所举办“学习三个代表重要思想、贯彻十六届三中全会决定、推进创业板市场建设研讨会”。</br>
2004年1月31日，国务院发布《关于推进资本市场改革开放和稳定发展的若干意见》，将发展资本市场提升到国家战略，提出九个方面的纲领性意见。特别针对创业板市场建设，提出“分步推进创业板市场建设，完善风险投资机制，拓展中小企业融资渠道”。</br>
2004年5月17日，经国务院同意，中国证监会批复同意在深交所设立中小企业板块。5月27日，中小企业板启动。6月25日，中小企业板首批8家公司上市。中小企业板规范发展，成为全球范围内最为成功的中小企业市场之一，中小企业板的成功实践为创业板推出开辟了道路。</br>
2005年4月29日，中国证监会正式启动股权分置改革。2006年底，深交所上市公司股权分置改革率先基本完成。股权分置改革、证券公司规范治理和上市公司综合治理解决了长期困扰资本市场发展的主要障碍，也为推出创业板进一步创造了条件。</br>
2008年，严重国际金融危机波及全球经济。为实现中国经济持续健康发展，各界呼吁尽快推出创业板。2008年3月21日，中国证监会就《首次公开发行股票并在创业板上市管理办法》向社会公开征求意见。2008年12月8日，国务院办公厅发布《关于当前金融促进经济发展的若干意见》，明确将“适时推出创业板”。</br>
2009年10月23日，创业板正式启动。10月30日，首批28家创业板上市公司集中上市。</br>

