深交所直面严峻形势，适应体制变化，推进市场服务工程，加强市场一线监管，实现从区域性市场到全国性市场的跨越性成长，完成由试验性探索到规范化运作的关键性转变。</br>

1995年12月，时任国务院副总理朱镕基提出“法制、监管、自律、规范”八字方针，作为证券市场发展的总体要求。</br>
